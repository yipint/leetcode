public class Solution_707 {
    class MyLinkedList {
        private int value;
        private MyLinkedList next;

        public MyLinkedList(int value, MyLinkedList next) {
            this.value = value;
            this.next = next;
        }

        public MyLinkedList() {

        }

        public int get(int index) {
            if (index == 1) {
                return value;
            }
            MyLinkedList cur = this;
            int begin = 1;
            while (cur != null && begin != index) {
                begin++;
                cur = cur.next;
            }
            return begin == index ? value : -1;
        }

        public void addAtHead(int val) {
            MyLinkedList cur = new MyLinkedList(this.value, this.next);
            this.value = val;
            this.next = cur;
        }

        public void addAtTail(int val) {
            MyLinkedList tmp = this;
            while (tmp.next != null) {
                tmp = tmp.next;
            }
            MyLinkedList cur = new MyLinkedList(val, null);
            tmp.next = cur;
        }

        public void addAtIndex(int index, int val) {
            if (index < 0) {
                addAtHead(val);
                return;
            }
            int begin = 1;
            MyLinkedList cur = this;
            while (begin < index && cur.next != null) {
                begin++;
                cur = cur.next;
            }
            if (begin < index) {
                return;
            }
            MyLinkedList next = cur.next;
            MyLinkedList tmp = new MyLinkedList(value, next);
            cur.next = tmp;
        }

        public void deleteAtIndex(int index) {
            if (index == 0) {
                return;
            }
            if (index == 1) {
                this.value = this.next.value;
                this.next = this.next.next;
            }
            MyLinkedList cur = new MyLinkedList(this.value, this.next);
            MyLinkedList pre = cur;
            int begin = 1;
            while (begin < index && cur.next != null) {
                begin++;
                pre = cur;
                cur = cur.next;
            }
            if (begin < index) {
                return;
            }
            pre.next = cur.next;
        }
    }
}
